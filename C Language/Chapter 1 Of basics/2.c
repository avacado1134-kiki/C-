#include <stdio.h>

int main()
{
    // Creating variables having different data types
    int integer = 26;
    float floating = 39.32;
    char character = 'A';
    // char c;
    // c='a'  (always const in single quote)

    // Printing variables with the help of their respective format specifiers
    printf("%d\n", integer);
    printf("%f\n", floating);
    printf("%c\n", character);
}
// \n is a newline character and it forces the cursor to change its position to the beginning of the next line on the screen